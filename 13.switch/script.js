// Comando switch
// Estrutura Condicional pouco utilizada.
// Necessidade de vários ifs (switch/case)

let nome = 'João'

switch (nome) {
    case 'Matheus':
        console.log('O nome é Matheus.')
        break

    case 'Vinícius':
        console.log('O nome é Vinícius.')
        break

    default:
        console.log('Nenhum nome foi declarado.')
        break
}

