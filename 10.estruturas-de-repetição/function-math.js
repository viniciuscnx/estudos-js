// built-in: Math.x()
// Muito usada para expressões matemáticas

let maiorNumero = Math.max(2,3,4,1)
console.log(`O maior num de 1 a 4 é ${maiorNumero}`)

let menorNumero = Math.min(2,3,4,1)
console.log(`O menor num de 1 a 4 é ${menorNumero}`)

let arredondar = Math.round(5.295948)
console.log(arredondar)

let arredondarParaCima = Math.ceil(5.295948)
console.log(arredondarParaCima)